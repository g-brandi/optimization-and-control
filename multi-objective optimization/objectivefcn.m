function f = objectivefcn(x)

% First objective function (costs)
f(1,1) =   2*x(1) +   3*x(2) +    4*x(3) +   3*x(4) + ... 
           2*x(5) +   1*x(6) +    1*x(7) +   4*x(8) + ...
           3*x(9) +  4*x(10) +   5*x(11) +  2*x(12) + ...
           10*x(13) + 30*x(14) +  20*x(15) + 10*x(16) + ...
           30*x(17) + 20*x(18) +  10*x(19) + 30*x(20) + ...
           20*x(21) + 10*x(22) +  30*x(23) + 20*x(24);
    
 % Second objective function (distances)
 f(2,1) = 40*x(13) + 70*x(14) + 100*x(15) + 80*x(16) + ...
         120*x(17) + 20*x(18) +  50*x(19) + 40*x(20) + ...
          90*x(21) + 60*x(22) +  50*x(23) + 30*x(24);
    
 % Third objective function (delivery schedule)
 f(3,1) = 70*x(13) + 30*x(14) +  25*x(15) + 50*x(16) + ...
          35*x(17) + 40*x(18) + 120*x(19) + 25*x(20) + ...
          10*x(21) + 40*x(22) +  20*x(23) + 30*x(24);
end